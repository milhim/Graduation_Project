	  if (b.isBipartite()) {
		NumberOfColor4.setText(2 + " Color to Coloring The Graph");
	  } else {
		NumberOfColor4.setText("not a bipartite  try another alg");

	  }
	  long end = System.nanoTime();
	  double totalTime = (double) (end - start) / 1000000000;
	  double modTime = (double) (end - start) % 1000000000;

	  time4.setText("Coloring toke : " + (int) totalTime + "." + (int) modTime + " sec. ");

