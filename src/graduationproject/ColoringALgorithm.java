    private void createRandomGraphActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createRandomGraphActionPerformed
	  Random v1 = new Random();
	  int size = Integer.parseInt(numberOfVertex.getText());
	  v = new VertexCase(size);
	  g = new GraphColoring(size);
	  b = new Bipartite(size);
	  int edgesNumber = Integer.parseInt(numberOfEdges.getText());
	  int x;
	  int y;
	  while (edgesNumber > 0) {
		x = v1.nextInt(size);
		y = v1.nextInt(size);

		if (x != y && v.undirectedGraph[x][y]==false && b.undirectedGraph[x][y]==false && g.graph[x][y]==0) {
		    v.addedge(x, y);
		    g.addedge(x, y);
		    b.addedge(x, y);
		    System.out.println("edge "+x+" and "+y);
		    edgesNumber--;
		}
	  }
	  // TODO add your handling code here:
    }//GEN-LAST:event_createRandomGraphActionPerformed

